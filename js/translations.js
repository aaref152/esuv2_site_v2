// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'en';
}

// Set language and save to localStorage
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    updatePageContent(lang);
}

// Update all page content based on selected language
function updatePageContent(lang) {
    const t = translations[lang];

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            // Check if it's an input placeholder
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = t[key];
            } else {
                // Preserve line breaks in content
                element.innerHTML = t[key].replace(/\n/g, '<br>');
            }
        }
    });

    // Update page title
    document.title = t.siteName;

    // Update document links based on language
    const privacyLink = document.querySelector('a[href*="privacy-policy"]');
    const userAgreementLink = document.querySelector('a[href*="user-agreement"]');
    const publicOfferLink = document.querySelector('a[href*="public-offer"]');

    if (lang === 'en') {
        if (privacyLink) privacyLink.href = '/privacy-policy-en.html';
        if (userAgreementLink) userAgreementLink.href = '/user-agreement-en.html';
        if (publicOfferLink) publicOfferLink.href = '/public-offer-en.html';
    } else if (lang === 'th') {
        // For Thai, use English versions for now (you can create Thai versions later)
        if (privacyLink) privacyLink.href = '/privacy-policy-en.html';
        if (userAgreementLink) userAgreementLink.href = '/user-agreement-en.html';
        if (publicOfferLink) publicOfferLink.href = '/public-offer-en.html';
    } else {
        // Default to original Russian documents
        if (privacyLink) privacyLink.href = '/privacy-policy.html';
        if (userAgreementLink) userAgreementLink.href = '/user-agreement.html';
        if (publicOfferLink) publicOfferLink.href = '/public-offer.html';
    }

    // Update language selector buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    const currentLang = getCurrentLanguage();
    updatePageContent(currentLang);

    // Add event listeners to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
});
